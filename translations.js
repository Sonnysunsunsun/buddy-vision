// Add fallback for all other language codes (use English as default)
const defaultTranslations = translations['en-US'];

/**
 * Get translations for a specific language
 * @param {string} languageCode - Language code (e.g., 'es-ES')
 * @returns {Object} Translations object
 */
function getTranslations(languageCode) {
    // Check if we have exact match
    if (translations[languageCode]) {
        return translations[languageCode];
    }

    // Try language prefix (e.g., 'es' from 'es-US')
    const langPrefix = languageCode.split('-')[0];

    // Find any translation that starts with this prefix
    for (const [code, trans] of Object.entries(translations)) {
        if (code.startsWith(langPrefix)) {
            return trans;
        }
    }

    // Fall back to English
    return defaultTranslations;
}

// Initialize and expose globally
window.buddyTranslations = {
    translations,
    getTranslations
};

